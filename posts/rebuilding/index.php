		<h1>Rebuilding</h1>

		<p>I'm in the process of rebuilding my personal site. It kinda sucks that I was more or less forced into it, but I have wanted to do it for a while. I'm taking it as an opportunity to do something very different.</p>

		<h2>CMS Free</h2>

		<p>It sounds crazy but I am not going to use any CMS on this site. I have been writing markup for about 11 years and the past 2 have been primarily in a Rails environment. Aside from that, I have some WordPress and ExpressionEngine experience. In all of these, you have to deal with a great deal of markup that isn't of your choosing. Things come in dynamically from the framework, gems, plugins, etc. Inspired by <a href="http://www.zeldman.com/2012/05/18/web-design-manifesto-2012">Zeldman's recent post</a>, I'm going back to my personal site being my playground and I want to choose all of the sand.</p>

		<h2>I don't recommend this to everyone</h2>

		<p>This is a personal preference and exploration and it is horribly inefficient for business purposes. However, working on my personal site is a hobby. It is a time for playing and for relaxation. I don't relax when I am pasting code into a WordPress input and reading about how to not have it wrap my sample &lt;script&gt; tags in a &lt;p&gt; tag. I debated on a static site generator but they have a weird feel about them too. Most want to lean towards markdown and have a long list of helpers to learn. For work and efficency these are very good things but I love markup and I want to write it. I just want to write it and I want every piece of markup in this site to be a conscious choice.</p>

		<h2>Building in Public</h2>

		<p>I don't get a lot of traffic, but those of you who made it here will get to see this process and I'll share how things go. Here are my to-dos:</p>

		<ol>
			<li>Build a list of all the things that I use to rebuild.</li>
			<li><strike>Set up Github, both for versioning and to deploy to the server.</strike></li>
			<li>Gather the resources that I know I'll need for design (fonts and images).</li>
			<li>Define how to structure the files.</li>
			<li>Design the nav.</li>
			<li>Design the basic post style.</li>
			<li>Get recent posts back up in the basic post style.</li>
		</ol>

		<p>That will get me started and I'll have a MVW (minimum viable website). Getting the recent posts back up are important to me because I had shared how much a few people had helped me recently and I want those back up so others know how awesome they are.</p>

		<h2>Thank you, kinda</h2>

		<p>So while it sucks that some script kiddies started injecting my database with malware, it has gotten me passionate about rebuilding my personal site. So thank you, kinda.</p>

