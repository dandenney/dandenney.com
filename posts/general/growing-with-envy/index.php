		<h2>Amazing culture</h2>
		<p>I know that words like 'awesome' and 'amazing' get thrown around a lot, but I am really amazed. I had no idea that there were web companies with a culture like this in Florida. The way projects are formed and assigned, the location, the internal communication and the amenities are all top-notch. It is truly a special company and I am already so proud to be a part of it. Honors go to <a href="http://twitter.com/greggpollack">Greg Pollack</a> for putting it all together and keeping it going.</p>

		<h2>Personal growth</h2>
		<p>I got a heads-up before I started about the tech that the front-end team uses so I jumped into learning Haml and Coffeescript. In a week, I'm already working writing Haml pretty comfortably. I can read Coffeescript but haven't written any yet outside of earning the badge. (We did have an awesome pairing session this morning, though.) </p>
		<p>I'm diving back into using Terminal more and learned a bit more about setting up local Rails instances. On top of this, I have learned a ton just by listening to the way that the team communicates and discusses problems to solve. I can't even fathom where my skill level will be in a few months.</p>

