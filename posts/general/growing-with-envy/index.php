		<h1>Growing with Envy</h1>
		<p>I recently signed on with the Envy Labs team as a front-end dev to work on projects including Code School. This is a little post sharing how big of a change this is for me and how excited I am.</p>

		<h2>I found my Wufoo</h2>
		<p>Ever since I met the whole <a href="http://wufoo.com/about">Wufoo team</a> at one of their weekly scrums, I've been saying that I wanted to "find my Wufoo". I was wowed by how they were a team of extremely talented individuals that worked together on a product that they cared about. </p>

		<p>I have known of <a href="http://envylabs.com">Envy Labs</a> for a while and paid attention to their client projects, community outreach and the team. What I've found in my first week is that they have all of the things that I have admired about that Wufoo team and they also work on a product that I love: <a href="http://codeschool.com">Code School</a>.</p>

